#include <iostream>
using namespace std;
int main()
{
   double x = 123561e-6;
   cout.precision(15);              // Precision 15
   cout << " By default:   " << x << endl;
   cout << " showpoint:  " << showpoint  << x << endl;
   cout << " fixed:      " << fixed      << x << endl;
   cout << " scientific: " << scientific << x << endl;
   return 0;
}  

